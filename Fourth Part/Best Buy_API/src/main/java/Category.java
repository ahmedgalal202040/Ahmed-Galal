public class Category {

    private String name;
    private String id;

    public String getID()
    {
        return id;
    }
    public void   setID(String id)
    {
        this.id =id;
    }
    public String getName()
    {
        return name;
    }
    public void   setName(String name)
    {
        this.name =name;
    }
}
